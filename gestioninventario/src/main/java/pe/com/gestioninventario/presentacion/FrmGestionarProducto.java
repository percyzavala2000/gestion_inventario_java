  public void cargarTabla() {
    DefaultTableModel modeloTabla = (DefaultTableModel) tabla_productos.getModel();
    modeloTabla.setRowCount(0);
   
    ProductoDAO productoDAO = new ProductoDAO();
    try {
      List<Producto> productos = productoDAO.mostrarProducto();
      for (Producto producto : productos) {
        Object[] fila = {producto.getId()+ "", producto.getNombre(), producto.getDescripcion(), producto.getPrecio()+"",
            producto.getCantidad()+"", producto.getCategoria(), producto.getMarca(), producto.getEstado()};
        modeloTabla.addRow(fila);
      }

    } catch (Exception e) {
      e.printStackTrace();
      JOptionPane.showMessageDialog(this, "Error al cargar tabla");

    }

  }

  private void btn_actualizarActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btn_actualizarActionPerformed
    ProductoDAO productoDAO = new ProductoDAO();
    int fila = tabla_productos.getSelectedRow();
    if (fila == -1) {
      JOptionPane.showMessageDialog(this, "Seleccione un producto");
    } else {
      int id = Integer.parseInt(txt_id.getText());
      String nombre = txt_nombre1.getText();
      String descripcion = txt_descripcion.getText();
      double precio = Double.parseDouble(txt_precio.getText());
      int cantidad = Integer.parseInt(txt_cantidad.getText());
      String categoria = ComboBox_categoria.getSelectedItem().toString();
      String marca = ComboBox_marca.getSelectedItem().toString();
      String estado = ComboBox_estado1.getSelectedItem().toString();
      Producto producto = new Producto(id, nombre, descripcion, precio, cantidad, categoria, marca, estado);
      try {
        productoDAO.actualizarProducto(producto);
        JOptionPane.showMessageDialog(this, "Producto actualizado correctamente");
        cargarTabla();
        limpiarCampos();
      } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error al actualizar producto");
      }
    }
   
  }// GEN-LAST:event_btn_actualizarActionPerformed

  private void btn_eliminarActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btn_eliminarActionPerformed
    ProductoDAO productoDAO = new ProductoDAO();
    int fila = tabla_productos.getSelectedRow();
    if (fila == -1) {
      JOptionPane.showMessageDialog(this, "Seleccione un producto");
    } else {
      int id = Integer.parseInt(tabla_productos.getValueAt(fila, 0).toString());
      Producto producto = new Producto(id);
      try {
        productoDAO.eliminarProducto(producto);
        JOptionPane.showMessageDialog(this, "Producto eliminado correctamente");
        cargarTabla();
      } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error al eliminar producto");
      }
    }
  }// GEN-LAST:event_btn_eliminarActionPerformed

