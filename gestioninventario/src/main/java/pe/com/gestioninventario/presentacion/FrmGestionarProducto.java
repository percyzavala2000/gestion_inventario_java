    private void tabla_productosMouseClicked(java.awt.event.MouseEvent evt) {    
      
    int fila = tabla_productos.getSelectedRow();
    if (fila == -1) {
      JOptionPane.showMessageDialog(this, "Seleccione un producto");
    } else {
      int id = Integer.parseInt(tabla_productos.getValueAt(fila, 0).toString());
      String nombre = tabla_productos.getValueAt(fila, 1).toString();
      String descripcion = tabla_productos.getValueAt(fila, 2).toString();
      String precio = tabla_productos.getValueAt(fila, 3).toString();
      String cantidad = tabla_productos.getValueAt(fila, 4).toString();
      String categoria = tabla_productos.getValueAt(fila, 5).toString();
      String marca = tabla_productos.getValueAt(fila, 6).toString();
      String estado = tabla_productos.getValueAt(fila, 7).toString();
      txt_id.setText(id + "");
      txt_nombre1.setText(nombre);
      txt_descripcion.setText(descripcion);
      txt_precio.setText(precio);
      txt_cantidad.setText(cantidad);
      ComboBox_marca.setSelectedItem(marca);
      ComboBox_categoria.setSelectedItem(estado);
      ComboBox_categoria.setSelectedItem(categoria);
    }
    

         
         
                                                   
    }                                            

    
