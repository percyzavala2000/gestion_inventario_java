    private void tabla_usuariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabla_usuariosMouseClicked
        UsuarioDAO usuarioDAO = new UsuarioDAO();
        int fila = tabla_usuarios.getSelectedRow();
        int id = Integer.parseInt(tabla_usuarios.getValueAt(fila, 0).toString());
        Usuario usuario = new Usuario(id);
        try {
          usuario = usuarioDAO.buscarUsuario(usuario);
          txtId_Usuario.setText(usuario.getId() + "");
          txtNombreCuenta.setText(usuario.getUsuario());
          txtClave.setText(usuario.getClave());
          txtNumeroDocumento.setText(usuario.getIdPersona() + "");
          com_box_Estado.setSelectedItem(usuario.getEstado());
        } catch (Exception e) {
          e.printStackTrace();
          JOptionPane.showMessageDialog(null, "Error al seleccionar el usuario");
        }
        


    }//GEN-LAST:event_tabla_usuariosMouseClicked

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        UsuarioDAO usuarioDAO = new UsuarioDAO();
        Usuario usuario = new Usuario();
        usuario.setId(Integer.parseInt(txtId_Usuario.getText()));
        usuario.setUsuario(txtNombreCuenta.getText());
        usuario.setClave(txtClave.getText());
        usuario.setIdPersona(Integer.parseInt(txtNumeroDocumento.getText()));
        usuario.setEstado(com_box_Estado.getSelectedItem().toString());
        try {
          usuarioDAO.actualizarUsuario(usuario);
          JOptionPane.showMessageDialog(null, "Usuario actualizado correctamente");
          cargarTabla();
          limpiar();
        } catch (Exception e) {
          e.printStackTrace();
          JOptionPane.showMessageDialog(null, "Error al actualizar usuario");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

