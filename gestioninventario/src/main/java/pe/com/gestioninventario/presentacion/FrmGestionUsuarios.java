    // cargar tabla
    public void cargarTabla() {
      DefaultTableModel modelo = (DefaultTableModel) tabla_usuarios.getModel();
      modelo.setRowCount(0);
        UsuarioDAO usuarioDAO = new UsuarioDAO();
        try {
          List<Usuario> lista = usuarioDAO.mostrarUsuario();
          for (Usuario usuario : lista) {
            Object[] fila = {usuario.getId(), usuario.getUsuario(), usuario.getApellidoPaterno() + " " + usuario.getApellidoMaterno() + " " + usuario.getNombres(), usuario.getEstado()};
            modelo.addRow(fila);
          }
        } catch (Exception e) {
          e.printStackTrace();

          JOptionPane.showMessageDialog(null, "Error al cargar la tabla");
          

        }
      
    }
